# Q4- What is the difference between AI, ML, DL, and DS?

# Answer - 

#  AI is the overarching field concerned with creating intelligent machines,
#  ML is a subset of AI focused on developing algorithms that learn from data, DL is a specialized form of ML using deep neural networks, 
# and DS is a multidisciplinary field that encompasses various techniques to analyze and extract insights from data. These fields often overlap, 
# with AI and ML being fundamental to many data science tasks, and deep learning being a specific ML technique used in various AI applications.