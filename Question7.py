# Q7- How can unsupervised learning be used in anomaly detection?

# Answer - 

# Unsupervised learning can be a valuable technique for anomaly detection because it allows you to identify patterns and 
# outliers in data without the need for labeled examples of anomalies.

# Unsupervised learning for anomaly detection is particularly useful when you have limited or no labeled data for anomalies, 
# making it suitable for scenarios where novel or unexpected anomalies need to be identified.