# Q5- What are the main difference betweenn supervised, unsupervised, and semi-supervised learning?

# Answer - 

# In the Supervised Learning we have input dataset and we also have the outputs. and, on the basis of that we will predict the algorithms.

# In the unsupervised learning the no. of output is given as a clusters/group of similar data and based on that we will predict the algorithms.

# semi-supervised learning is the combination of supervised and unsupervised learning