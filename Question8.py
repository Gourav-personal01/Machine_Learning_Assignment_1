# Q8- List down some commonly used supervised learning algorithms and unsupervised learning
# algorithms.

# Answer - 

# Supervised Learning -

# Linear Regression: Used for regression tasks, where the goal is to predict a continuous numerical value.

# Logistic Regression: Primarily used for binary classification problems, where the goal is to classify data into two classes.

# Decision Trees: Tree-like structures used for both classification and regression tasks, capable of handling both categorical and numerical data.

# Unsupervised Learning -

# K-Means Clustering: Used to partition data into clusters based on similarity, often employed for data segmentation.

# Hierarchical Clustering: Builds a tree-like hierarchy of clusters, useful for visualizing data relationships.

# DBSCAN (Density-Based Spatial Clustering of Applications with Noise): Clustering method that can discover clusters of arbitrary shapes and identify noise points